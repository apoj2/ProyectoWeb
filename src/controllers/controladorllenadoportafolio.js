
export function llenarPortafolio(productos){
	let fotoportafolio1=document.getElementById('fotosportafolio1')


    productos.forEach(function(fotoprincipal1) {
	
	    let columna1 = document.createElement("div")
	    columna1.classList.add("col","my-3")

	    let tarjeta1=document.createElement("div")
	    tarjeta1.classList.add("card","w-100","text-center","shadow")

        let fotoproducto1=document.createElement("img")
	    fotoproducto1.classList.add("img-fluid","w-100")
	    fotoproducto1.src=fotoprincipal1.foto[0]

		//fotoproducto1.addEventListener("mouseover",function(evento){
          //    fotoproducto1.src=fotoprincipal1.foto[1]
		//})
		//fotoproducto1.addEventListener("mouseleave",function(evento){
		//	fotoproducto1.src=fotoportafolio1.foto[0]
		//})
 
        tarjeta1.appendChild(fotoproducto1)
	    columna1.appendChild(tarjeta1)
	    fotoportafolio1.appendChild(columna1)
});
}
